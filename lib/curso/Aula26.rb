#Números

#Inteiros
    x = 1 #pertence a classe fixnum
    x2 = 100000000000000000000000000000000 # pertence a classe bignum

#Binários
    x3 = 0b1010 #faz isso para apresentar um número binário, transferindo ele para decimal (0b)
    puts x3

#Octal
    x4 = 0o45 #faz isso para apresentar um número Octal, transferindo ele para decimal (0o)
    puts x4

#Hexadecimal
    x5 = 0xAF #faz isso para apresentar um número Hexadecimal, transferindo ele para decimal (0o)
    puts x5

#Usando Underline para deixar o número mais legivel em código
    x6 = 1_000_000 
    puts x6

#Float
    x7 = 1.5
    puts x7

    x8 = 100 + 1.5
    puts x8